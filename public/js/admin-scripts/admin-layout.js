/* Name click js */ 
document.getElementById('name-admin').addEventListener('click',()=>{
    let nameBox = document.getElementById('name-box');
    if(nameBox.style.display == "none"){
        nameBox.style.display = "block";
    }
    else{
        nameBox.style.display = "none";
    }
})

